// #########( Hairs Filters )###########


function setBlueHoodieFilter(){
    let all = document.getElementsByClassName('img-box');

    for(let i=0;i<all.length;i++)
    {
        all[i].style.display = "none";
    }

    let hoodie = document.getElementsByClassName('hoodie');
    hoodie[0].style.display = "block";
}

