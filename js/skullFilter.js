// #########( Skull Filters )###########


function setBlueSkullFilter(){
    let all = document.getElementsByClassName('img-box');

    for(let i=0;i<all.length;i++)
    {
        all[i].style.display = "none";
    }

    let skull = document.getElementsByClassName('skull');
    skull[0].style.display = "block";
}


